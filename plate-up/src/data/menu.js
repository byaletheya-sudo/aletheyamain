export function makeSoftServeItem(flavor, quality) {
  const flavorNames = { vanilla: 'Vanilla', chocolate: 'Chocolate', swirl: 'Chocolate-Vanilla Swirl' }
  const qualityText = {
    perfect: 'A flawless swirl with the little curl on top. Frame it. Post it. Tell your grandkids.',
    good: 'A respectable swirl. Doug would nod at this. Not effusively, but he would nod.',
    weak: 'A modest little dollop. You panicked and let go early. It happens to everyone once.',
    collapsed: 'Structural failure. A classic. You held on too long and the tower paid the price.',
  }
  return {
    id: `soft-serve-${flavor}`,
    name: `${flavorNames[flavor]} Soft Serve`,
    station: 'dessert-bar',
    emoji: '🍦',
    size: 2,
    category: 'dessert',
    tags: quality === 'perfect' ? ['kid', 'classic', 'goat-tier'] : ['kid', 'classic'],
    softServe: { flavor, quality },
    flavorText: qualityText[quality],
  }
}

